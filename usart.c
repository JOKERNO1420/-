void u2_printf(char* fmt,...)  
{  
	
	va_list ap;
	
	va_start(ap,fmt);
	vsprintf((char*)USART2_TX_BUF,fmt,ap);
	va_end(ap);
	
//  HAL_UART_Transmit_DMA(&huart2,USART2_TX_BUF,strlen((const char*)USART2_TX_BUF));//开启DMA传输
	HAL_UART_Transmit(&huart2,USART2_TX_BUF,strlen((const char*)USART2_TX_BUF),8000);
	while(HAL_UART_GetState(&huart2) == HAL_UART_STATE_BUSY_TX);//检测UART发送结束
	USART2_TX_STA=1;
  HAL_UART_Receive_IT(&huart2, (uint8_t *)&aRxBuffer, 1);   //再开启接收中断
}



